function add(num1, num2) {
    return num1 + num2; //add num1 and num2
}
console.log(add(4, 2)); //Test Add function

function subtract(num1, num2) {
    return num1 - num2; //subtract num2 from num1
}
console.log(subtract(4, 2)); //Test subtract function

function multiply(num1, num2) {
    return num1 * num2; //multiply num1 by num2
}
console.log(multiply(4, 2)) //Test multiply function

function divide(num1, num2) {
    return num1 / num2; //divide num1 by num2
}
console.log(divide(4, 2)) //Test Divide function